# ─── helpers ───────────────────────────────────────────
def _font(run, cn="宋体", en="Times New Roman", sz=Pt(12)):
    run.font.size = sz
    run.font.name = en
    rPr = run._element.get_or_add_rPr()
    rFonts = rPr.find(qn("w:rFonts"))
    if rFonts is None:
        rFonts = OxmlElement("w:rFonts")
        rPr.insert(0, rFonts)
    rFonts.set(qn("w:eastAsia"), cn)
    rFonts.set(qn("w:ascii"), en)
    rFonts.set(qn("w:hAnsi"), en)

def body(text, cn="宋体", en="Times New Roman", sz=Pt(12),
         bold=False, align=None, indent=Cm(0.74)):
    p = doc.add_paragraph()
    if align is not None:
        p.alignment = align
    pf = p.paragraph_format
    pf.line_spacing = Pt(20)
    pf.space_before = Pt(0)
    pf.space_after = Pt(0)
    if indent:
        pf.first_line_indent = indent
    r = p.add_run(text)
    r.bold = bold
    _font(r, cn, en, sz)
    return p

def heading1(text):
    return body(text, cn="黑体", sz=Pt(15), align=WD_ALIGN_PARAGRAPH.CENTER, indent=None)

def heading2(text):
    return body(text, cn="黑体", sz=Pt(14), bold=True, indent=None)

def heading3(text):
    return body(text, cn="黑体", sz=Pt(12), bold=True)

def blank():
    body("", indent=None)

def set_cell(cell, text, cn="宋体", en="Times New Roman", sz=Pt(10.5)):
    cell.text = ""
    p = cell.paragraphs[0]
    r = p.add_run(text)
    _font(r, cn, en, sz)

